    public void computePI(int interactions, int deadline){
        Random r = new Random();
        Request message = new Request("request",id,request_number,01,interactions,0.0,deadline,portaCliente);
        echo.sendMessage(message);
        setMessageToSend(message);
        request_number++;
    }

